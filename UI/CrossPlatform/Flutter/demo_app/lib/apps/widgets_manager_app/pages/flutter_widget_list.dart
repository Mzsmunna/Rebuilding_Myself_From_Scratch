      tileColor: Colors.white30,
      onTap: () {
        //print("navgating to...");
        Navigator.of(context).push(MaterialPageRoute(builder: (context) {
          return Scaffold(
            appBar: AppBar(
              title: Text(title),
            ),
            body: bindWidget,
          );
        }));
